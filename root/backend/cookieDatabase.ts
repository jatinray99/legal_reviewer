export type CookieDBEntry = {
    id: string;
    category: string;
    cookie: string;
    domain: string;
    description: string;
    retentionPeriod: string;
    dataController: string;
    privacyLink: string;
    wildcardMatch: string;
};

export function findCookieInDatabase(cookieName: string): CookieDBEntry | null {
    const lowerCaseCookieName = cookieName.toLowerCase();
    for (const provider in cookieDatabaseData) {
        for (const entry of (cookieDatabaseData as any)[provider] as CookieDBEntry[]) {
            const dbCookieName = entry.cookie.toLowerCase();
            if (entry.wildcardMatch === "1") {
                if (lowerCaseCookieName.startsWith(dbCookieName)) {
                    return entry;
                }
            } else {
                if (lowerCaseCookieName === dbCookieName) {
                    return entry;
                }
            }
        }
    }
    return null;
}
